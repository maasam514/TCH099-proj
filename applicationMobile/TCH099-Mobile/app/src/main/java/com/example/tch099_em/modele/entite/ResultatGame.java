package com.example.tch099_em.modele.entite;

public class ResultatGame {
    private int idGame, scoreEquipeDom, scoreEquipeExterieur, passes, carteJaune, carteRouge;

    public int getIdGame() {
        return idGame;
    }

    public void setIdGame(int idGame) {
        this.idGame = idGame;
    }

    public int getScoreEquipeDom() {
        return scoreEquipeDom;
    }

    public void setScoreEquipeDom(int scoreEquipeDom) {
        this.scoreEquipeDom = scoreEquipeDom;
    }

    public int getScoreEquipeExterieur() {
        return scoreEquipeExterieur;
    }

    public void setScoreEquipeExterieur(int scoreEquipeExterieur) {
        this.scoreEquipeExterieur = scoreEquipeExterieur;
    }

    public int getPasses() {
        return passes;
    }

    public void setPasses(int passes) {
        this.passes = passes;
    }

    public int getCarteJaune() {
        return carteJaune;
    }

    public void setCarteJaune(int carteJaune) {
        this.carteJaune = carteJaune;
    }

    public int getCarteRouge() {
        return carteRouge;
    }

    public void setCarteRouge(int carteRouge) {
        this.carteRouge = carteRouge;
    }
}
